package host.plas.bou.items.droppable;

/**
 * Enum defining the available keyed item types for droppable items.
 * These represent special item types that are created programmatically
 * rather than from NBT strings.
 */
public enum DroppableItemKeyedType {
    /** A jump boost potion item type. */
    POTION_JUMP,
    /** A levitation potion item type. */
    POTION_LEVITATION,
    /** An enchanted book item type. */
    ENCHANTED_BOOK,
    ;
}
